print("Type casting also known as type conversion"
      "which is also defined as conversion of one data type into another.")

print("Explicit is a type of conversion which is done by programmer.")
print("This is the example of explict type of conversion:")
a="1"
b=2
print("The sum is :", int(a)+b)


print("\nImplicit is a type of conversion which is done by compiler itself.")
print("This is the example of implicit type of conversion:\n")
A=1.9
B=1   #int value is changed to float  not to lose any data.
print("The sum is :", A+B)




